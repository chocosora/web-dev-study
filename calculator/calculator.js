const calculator = {
  plus: function (a, b) {
    console.log(a + b);
  },

  minus: function (a, b) {
    console.log(a - b);
  },

  divide: function (a, b) {
    console.log(a / b);
  },
  power: function (a, b) {
    console.log(a ** b);
  },
};

calculator.plus(2, 2);
calculator.minus(2, 2);
calculator.divide(2, 2);
calculator.power(2, 2);
